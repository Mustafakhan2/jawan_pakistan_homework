// Write a Dart program that uses a for loop to print the numbers from 1 to 10,
// each on a new line. Modify the program to print the numbers in reverse order, from 10 to 1.

void main() {
  // Printing numbers from 1 to 10
  for (int i = 1; i <= 10; i++) {
    print(i);
  }

  print('\n');

  // Printing numbers from 10 to 1
  for (int i = 10; i >= 1; i--) {
    print(i);
  }
}
